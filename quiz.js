const startBtn = document.getElementById("start");
const selectBtn = document.getElementById("select");
const playAgainBtn = document.getElementById("play-again");
const container = document.querySelector(".container");
const startScreen = document.querySelector(".start");
const resultScreen = document.querySelector(".result");

const questionEl = document.getElementById("question");
const labels = [
  document.getElementById("lb1"),
  document.getElementById("lb2"),
  document.getElementById("lb3"),
  document.getElementById("lb4")
];
const radios = [
  document.getElementById("opt1"),
  document.getElementById("opt2"),
  document.getElementById("opt3"),
  document.getElementById("opt4")
];

let currentQuestionIndex = 0;
let score = 0;
let quizQuestions = []; // store 10 random questions

// Pick 10 random questions
function pickRandomQuestions() {
  const copy = [...btsQuiz];
  quizQuestions = [];
  for (let i = 0; i < 10; i++) {
    const randomIndex = Math.floor(Math.random() * copy.length);
    quizQuestions.push(copy[randomIndex]);
    copy.splice(randomIndex, 1); // remove so it's not repeated
  }
}

// Start quiz
startBtn.addEventListener("click", () => {
  score = 0;
  currentQuestionIndex = 0;
  pickRandomQuestions(); // new random set each time
  startScreen.classList.add("off");
  container.classList.remove("off");
  loadQuestion();
});

// Load question
function loadQuestion() {
  const q = quizQuestions[currentQuestionIndex];
  questionEl.textContent = q.question;
  for (let i = 0; i < 4; i++) {
    labels[i].textContent = q.options[i];
    radios[i].value = q.options[i];
    radios[i].checked = false;
  }
}

// Get selected option
function getSelectedOption() {
  const selected = document.querySelector('input[name="option"]:checked');
  return selected ? selected.value : null;
}

// Submit answer
selectBtn.addEventListener("click", () => {
  const chosen = getSelectedOption();
  if (!chosen) return alert("Please select an option!");

  if (chosen === quizQuestions[currentQuestionIndex].correctAnswer) {
    score++;
  }

  currentQuestionIndex++;

  if (currentQuestionIndex < quizQuestions.length) {
    loadQuestion();
  } else {
    container.classList.add("off");
    resultScreen.classList.remove("off");
    document.getElementById("result").textContent = `Your score is: ${score}/10`;
  }
});

// Play again
playAgainBtn.addEventListener("click", () => {
  resultScreen.classList.add("off");
  container.classList.remove("off");
  score = 0;
  currentQuestionIndex = 0;
  pickRandomQuestions();
  loadQuestion();
});
